n = int(input())
pattern = "%d.%m.%Y"
pattern_dt = datetime.strftime
data = {}
for _ in range(n):
    *name, birthday = input().split()
    birthday = datetime.strptime(birthday, pattern)
    if birthday in data:
        data[birthday] += 1
    else:
        data[birthday] = 1
